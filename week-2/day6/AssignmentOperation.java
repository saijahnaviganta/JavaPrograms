class AssignmentOperation
  {
    public static void main(String args[])
    {
      int a=20;
      System.out.println("a+=5 value is "+(a+=5));
      System.out.println("a-=10 value is "+(a-=10));
       System.out.println("a*=2 value is "+(a*=2));
       System.out.println("a/=5 value is "+(a/=5));
       System.out.println("a%=5 value is "+(a%=5));
    }
  }