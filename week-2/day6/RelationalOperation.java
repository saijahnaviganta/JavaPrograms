 class RelationalOperation
   {
     public static void main(String args[])
     {
     System.out.println("less than(<):"+(3<5));
     System.out.println("less than or equal to(<=):"+(3<=5));
     System.out.println("Greater than(>):"+(3>5));
     System.out.println("Greater than or equal to:"+(3>=5));
     System.out.println("Double equal to:"+(3==5));
     System.out.println("Not equal to:"+(3!=5));
}
   }