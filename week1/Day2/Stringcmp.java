class Stringcmp
  {
    public static void main(String args[])
    {
      String name="Jahnavi";
      if(name.equals("Rajeswari"))
      {
        System.out.println("string is Rajeswari");
      }
      else if(name.equals("Meghana"))
      {
        System.out.println("string is meghana");
      }
      else if(name.equals("Jahnavi"))
      {
        System.out.println("string is jahnavi");
      }
      else if(name.equals("swetha"))
      {
        System.out.println("string is swetha");
      }
      else if(name.equals("latha"))
      {
        System.out.println("string is latha");
      }
      else 
        System.out.println("Invalid string");
    }
  }